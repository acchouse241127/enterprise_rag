DEFAULT_PROMPT_VERSION = "C"


def get_system_prompt(version: str | None) -> str:
    """Get system prompt by version. Falls back to C if invalid."""
    key = (version or "").strip().upper()
    if key in SYSTEM_PROMPT_VERSIONS:
        return SYSTEM_PROMPT_VERSIONS[key]
    return SYSTEM_PROMPT_VERSIONS[DEFAULT_PROMPT_VERSION]
